def area(a):
    """
    Возвращает площадь квадрата

            Параметры:
                    a (float): сторона квадрата

            Возвращаемое значение:
                    a * а (float): площадь квадрата
    """
    return a * a


def perimeter(a):
    """
    Возвращает периметр квадрата

            Параметры:
                    a (float): сторона квадрата

            Возвращаемое значение:
                    4 * ф (float): периметр квадрата
    """
    return 4 * a
